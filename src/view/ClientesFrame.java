    private void btnSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarClienteActionPerformed
   String nome = txtNome.getText().trim();
    if (nome.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Nome é obrigatório.", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    model.Cliente c = new model.Cliente();
    c.setNome(nome);
    c.setEndereco(txtEndereco.getText().trim());
    c.setTelefone(txtTelefone.getText().trim());
    c.setEmail(txtEmail.getText().trim());
    c.setCpf(txtCpf.getText().trim());
    c.setRg(txtRg.getText().trim());

    // --- Início da correção para a data ---
    String dataNascString = txtDataNasc.getText().trim();
    if (!dataNascString.isEmpty()) {
        SimpleDateFormat formatoEntrada = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat formatoSaida = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Date data = formatoEntrada.parse(dataNascString);
            String dataFormatada = formatoSaida.format(data);
            c.setDataNascimento(dataFormatada);
        } catch (ParseException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Formato de data inválido. Use dd/MM/yyyy.", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
    } else {
        c.setDataNascimento(null); // Ou um valor padrão, se necessário
    }
    // --- Fim da correção para a data ---

    app.AppContext.clienteDAO.add(c);
    refreshClienteTable();
    clearForm();

    }//GEN-LAST:event_btnSalvarClienteActionPerformed

    private void btnRemoverClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverClienteActionPerformed
        int row = tblClientes.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    int id = (int) tblClientes.getValueAt(row, 0);
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Confirma remoção do cliente selecionado?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        app.AppContext.clienteDAO.remove(id);
        refreshClienteTable();
    }

    }//GEN-LAST:event_btnRemoverClienteActionPerformed

