private void refreshClienteTable() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblClientes.getModel();
    model.setRowCount(0);
    for (model.Cliente c : app.AppContext.clienteDAO.getAll()) {
        model.addRow(new Object[]{c.getId(), c.getNome(), c.getEmail(), c.getTelefone()});
    }
}

